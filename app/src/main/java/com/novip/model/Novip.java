package com.novip.model;

public class Novip {

    private String host;
    private int port;
    private String admin_qq;
    private String qq_group;
    private String buy_url;

    public String getHost() {
        return host;
    }

    public void setHost(String host) {
        this.host = host;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }

    public String getAdmin_qq() {
        return admin_qq;
    }

    public void setAdmin_qq(String admin_qq) {
        this.admin_qq = admin_qq;
    }

    public String getQq_group() {
        return qq_group;
    }

    public void setQq_group(String qq_group) {
        this.qq_group = qq_group;
    }

    public String getBuy_url() {
        return buy_url;
    }

    public void setBuy_url(String buy_url) {
        this.buy_url = buy_url;
    }
}
